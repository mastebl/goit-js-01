console.log('primitives');

//STRING
let stringDate = 'string data';
console.log (stringDate);
// typeof - оператор определения типа данных
console.log(typeof stringDate);

stringDate = '';
console.log (stringDate);
console.log(typeof stringDate);

stringDate = ' ';
console.log (stringDate);
console.log(typeof stringDate);

//NUMBER
let number = 1;
console.log(number);
console.log(typeof number);

number = 2e53;
console.log(number);
console.log(typeof number);

number = Infinity;
console.log(number);
console.log(typeof number);

number = NaN;
console.log(number);
console.log(typeof number);

//BOLEAN true || false
let isOnline = true;
console.log(isOnline);
console.log(typeof isOnline);

isOnline = false;
console.log(isOnline);
console.log(typeof isOnline);

//UNDEFINED
let message;
console.log(message);
console.log(typeof message);

//NULL
let user = null;
console.log(user);
console.log(typeof user);